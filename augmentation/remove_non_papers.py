import os

def remove_paper(year, doi):
    fn = doi.replace('/', '+') + '.json'
    fn = os.path.join('..', 'data', 'papers', str(year), fn)
    if os.path.exists(fn):
        os.remove(fn)
        print('deleted', fn)
    else:
        print('already deleted', fn)

